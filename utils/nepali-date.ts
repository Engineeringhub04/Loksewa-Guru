function isLeapYear(year: number): boolean {
    return (year % 4 === 0 && year % 100 !== 0) || (year % 400 === 0);
}

function getNepaliMonthInString(month: number): string {
    const months = ["बैशाख", "जेष्ठ", "असार", "श्रावन", "भाद्र", "असोज", "कार्तिक", "मंग्सिर", "पौष", "माघ", "फाल्गुन", "चैत्र"];
    return months[month - 1] || "";
}

function getEnglishMonth(month: number): string {
    const months = ["Baisakh", "Jesth", "Asar", "Srawan", "Bhadra", "Aaswin", "Kartik", "Mangsir", "Paush", "Magh", "Falgun", "Chaitra"];
    return months[month - 1] || "";
}

function getNepaliDayOfWeekInString(day: number): string {
    const days = ["", "आइतबार ", "सोमबार ", "मंगलबार ", "बुधबार ", "बिहिबार ", "शुक्रबार ", "शनिबार "];
    return days[day] || "";
}

function getEnglishDayOfWeekInString(day: number): string {
    const days = ["", "Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday"];
    return days[day] || "";
}

export function convertEnglishDateToNepali(yy: number, mm: number, dd: number): [string, string] {
    if (!checkIfDateIsInRange(yy, mm, dd)) {
        return ["Invalid date!", "अवैध मिति!"];
    }

    const month = [31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31];
    const leap_year_months = [31, 29, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31];

    const def_eyy = 1944;
    const def_nyy = 2000;
    const def_nmm = 9;
    const def_ndd = 17 - 1;
    let total_eDays = 0;

    let day = 7 - 1;
    let i, j;

    for (i = 0; i < (yy - def_eyy); i++) {
        const monthsArray = isLeapYear(def_eyy + i) ? leap_year_months : month;
        for (j = 0; j < 12; j++) total_eDays += monthsArray[j];
    }

    for (i = 0; i < (mm - 1); i++) {
        total_eDays += isLeapYear(yy) ? leap_year_months[i] : month[i];
    }

    total_eDays += dd;

    i = 0; j = def_nmm; 
    let total_nDays = def_ndd;
    let m = def_nmm; 
    let y = def_nyy; 
    let a = 0;

    while (total_eDays !== 0) {
        a = nepali_years_and_days_in_months[i][j];
        total_nDays++;
        day++;
        if (total_nDays > a) {
            m++;
            total_nDays = 1;
            j++;
        }
        if (day > 7) day = 1;
        if (m > 12) { y++; m = 1; }
        if (j > 12) { j = 1; i++; }
        total_eDays--;
    }

    const dateString: [string, string] = [
        y + " " + getEnglishMonth(m) + " " + total_nDays + ", " + getEnglishDayOfWeekInString(day),
        y + " " + getNepaliMonthInString(m) + " " + total_nDays + ", " + getNepaliDayOfWeekInString(day)
    ];
    return dateString;
}

function checkIfDateIsInRange(year: number, month: number, day: number): boolean {
    if (year < 1944 || year > 2033) return false;
    if (month < 1 || month > 12) return false;
    if (day < 1 || day > 31) return false;
    return true;
}

function englishToNepaliNumber(number: string): string {
    const map: Record<string, string> = {
        "0": "०", "1": "१", "2": "२", "3": "३", "4": "४",
        "5": "५", "6": "६", "7": "७", "8": "८", "9": "९"
    };
    return map[number] || number;
}

function isNumeric(n: string): boolean {
    return !isNaN(parseFloat(n)) && isFinite(Number(n));
}

export function localizeNumber(temp: string): string {
    let result = "";
    for (let i = 0; i < temp.length; i++) {
        result += isNumeric(temp[i]) ? englishToNepaliNumber(temp[i]) : temp[i];
    }
    return result;
}
